CREATE TABLE SUBSCRIBERS
(
    ID     VARCHAR(36) NOT NULL,
    CNP    VARCHAR(100),
    NAME VARCHAR(100),
    ADDRESS VARCHAR(200),
    PHONE_NUMBER VARCHAR(15),
    USERNAME VARCHAR (50) NOT NULL UNIQUE,
    PASSWORD VARCHAR (500) NOT NULL,
    ROLE VARCHAR(15),
    FOREIGN KEY (ROLE) REFERENCES ROLE (TITLE),
    PRIMARY KEY (ID)
);