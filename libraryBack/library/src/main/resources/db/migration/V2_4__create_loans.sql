CREATE TABLE LOANS(
    ID VARCHAR (36),
    ID_BOOK VARCHAR(36),
    ID_SUBSCRIBER VARCHAR(36),
    LOAN_STATUS VARCHAR (15),
    PRIMARY KEY(ID),
    FOREIGN KEY (ID_BOOK) REFERENCES BOOKS (ID),
    FOREIGN KEY (ID_SUBSCRIBER) REFERENCES SUBSCRIBERS (ID),
    FOREIGN KEY (LOAN_STATUS) REFERENCES LOAN_STATUS (TITLE)
);